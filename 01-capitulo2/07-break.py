#
# Exemplo de como usar os comando Break e Continue
#

def loopBreak():
    for x in range(5,10):
        if(x == 7):
            break
        print("O valor do x é:" , x)
loopBreak()


def loopBreak():
    for c in range(5,10):
        if(c == 7):
            continue
        print("O valor do x é:" , c)
loopBreak()


